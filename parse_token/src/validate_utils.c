#include "../include/parse.h"

void jump_quotes(char *cmd_buff, int *i)
{
	if (cmd_buff[*i] == '"')
	{
		while (cmd_buff[*i + 1] != '"' && cmd_buff[*i + 1])
			*i = *i + 1;
	}
}

